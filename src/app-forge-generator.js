function transformIndex(source,profile,build){
  return source
    .replace(/<title>[^<]*<\/title>/,`<title>${html(profile.name)}</title>`)
    .replace(/(<meta name="apple-mobile-web-app-title" content=")[^"]*(")/,`$1${html(profile.shortName||profile.name)}$2`)
    .replace(/<span>FIRE<\/span><b>VAULT<\/b>/g,wordmarkMarkup(profile))
    .replace(/alt="FireVault"/g,`alt="${html(profile.name)}"`)
    .replace(/FireVault could not finish opening/g,`${html(profile.name)} could not finish opening`)
    .replace(/Your account data remains stored/g,`Your ${(profile.terminology?.recordSingular||"record").toLowerCase()} data remains stored`)
    .replace(/0\.99\.0/g,String(build));
}

function namespaceText(source,profile){
  if(profile.id==="firevault")return source;
  const ns=productNamespace(profile.id);
  return source.replaceAll("firevault_",`${ns}_`).replaceAll("FIREVAULT_",`${ns.toUpperCase()}_`);
}

function transformSource(path,source,plan,build){
  const profile=plan.profile;
  if(path==="src/generated-app-profile.js")return generatedProfileModule(profile);
  if(path==="manifest.json")return json(generatedManifest(profile,build));
  if(path==="version.json")return json(generatedVersion(profile,build,plan));
  if(path==="README.md")return generatedReadme(plan,build);
  if(path==="VALIDATION.md")return generatedValidation(plan);
  if(path==="architecture/app-profile.json")return json(profile);
  if(path==="architecture/app-forge-blueprint.json")return json(plan.manifest.blueprint);
  let text=path==="index.html"?transformIndex(source,profile,build):source.replace(/0\.99\.0/g,String(build));
  if(path==="sw.js"&&profile.id!=="firevault"){
    const cachePrefix=String(profile.id).toLowerCase().replace(/[^a-z0-9-]+/g,"-").replace(/^-+|-+$/g,"")||"app";
    text=text.replace("const CACHE=`firevault-${BUILD}`",`const CACHE=\`${cachePrefix}-\${BUILD}\``).replace('k.startsWith("firevault-")',`k.startsWith("${cachePrefix}-")`);
  }
  return namespaceText(text,profile);
}

