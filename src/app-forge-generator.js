async function defaultSourceLoader(path){
  const response=await fetch(new URL(path,document.baseURI),{cache:"no-store"});
  if(!response.ok)throw new Error(`Could not load ${path} (${response.status}).`);
  return new Uint8Array(await response.arrayBuffer());
}

function crcTable(){const table=new Uint32Array(256);for(let n=0;n<256;n++){let c=n;for(let k=0;k<8;k++)c=(c&1)?0xedb88320^(c>>>1):c>>>1;table[n]=c>>>0;}return table;}
const CRC_TABLE=crcTable();
function crc32(data){let crc=0xffffffff;for(const value of data)crc=CRC_TABLE[(crc^value)&255]^(crc>>>8);return (crc^0xffffffff)>>>0;}
function write16(view,offset,value){view.setUint16(offset,value,true);}
function write32(view,offset,value){view.setUint32(offset,value>>>0,true);}
function concat(chunks,total){const out=new Uint8Array(total);let offset=0;for(const chunk of chunks){out.set(chunk,offset);offset+=chunk.length;}return out;}

export function createDeterministicZip(entries=[]){
  const rows=entries.map(entry=>({path:String(entry.path).replace(/^\/+/,""),data:bytes(entry.data)})).sort((a,b)=>a.path.localeCompare(b.path));
  const locals=[];const centrals=[];let localSize=0;let centralSize=0;
  const dosTime=(12<<11);const dosDate=((2026-1980)<<9)|(7<<5)|15;
  for(const row of rows){
    const name=encoder.encode(row.path);const crc=crc32(row.data);const local=new Uint8Array(30+name.length);const lv=new DataView(local.buffer);
    write32(lv,0,0x04034b50);write16(lv,4,20);write16(lv,6,0x0800);write16(lv,8,0);write16(lv,10,dosTime);write16(lv,12,dosDate);write32(lv,14,crc);write32(lv,18,row.data.length);write32(lv,22,row.data.length);write16(lv,26,name.length);write16(lv,28,0);local.set(name,30);
    const central=new Uint8Array(46+name.length);const cv=new DataView(central.buffer);
    write32(cv,0,0x02014b50);write16(cv,4,20);write16(cv,6,20);write16(cv,8,0x0800);write16(cv,10,0);write16(cv,12,dosTime);write16(cv,14,dosDate);write32(cv,16,crc);write32(cv,20,row.data.length);write32(cv,24,row.data.length);write16(cv,28,name.length);write16(cv,30,0);write16(cv,32,0);write16(cv,34,0);write16(cv,36,0);write32(cv,38,0);write32(cv,42,localSize);central.set(name,46);
    locals.push(local,row.data);centrals.push(central);localSize+=local.length+row.data.length;centralSize+=central.length;
  }
  const end=new Uint8Array(22);const ev=new DataView(end.buffer);write32(ev,0,0x06054b50);write16(ev,4,0);write16(ev,6,0);write16(ev,8,rows.length);write16(ev,10,rows.length);write32(ev,12,centralSize);write32(ev,16,localSize);write16(ev,20,0);
  return concat([...locals,...centrals,end],localSize+centralSize+end.length);
}

export async function generateAppForgePwaPackage(recipeId="firevault",build="1.03.7",overrides={},options={}){
  const plan=appForgeGeneratorPlan(recipeId,build,overrides);if(!plan)throw new Error("The selected Product Recipe is unavailable.");
  if(!plan.packageReady)throw new Error("The generation request or App Profile did not pass validation.");
  const loader=options.sourceLoader||defaultSourceLoader;const onProgress=typeof options.onProgress==="function"?options.onProgress:()=>{};const entries=[];
  for(let index=0;index<APP_FORGE_CORE_SOURCE_FILES.length;index++){
    const path=APP_FORGE_CORE_SOURCE_FILES[index];onProgress({phase:"source",current:index+1,total:APP_FORGE_CORE_SOURCE_FILES.length,path});
    const loaded=bytes(await loader(path));
    entries.push({path:`app/${path}`,data:isText(path)?transformSource(path,decoder.decode(loaded),plan,build):loaded});
  }
  entries.push(
    {path:"factory/generation-request.json",data:json(plan.manifest.request)},
    {path:"factory/factory-manifest.json",data:json(plan.manifest)},
    {path:"factory/generator-plan.json",data:json({...plan,profile:undefined,manifest:undefined})},
    {path:"factory/package-report.json",data:json({kind:APP_FORGE_GENERATOR_KIND,schemaVersion:APP_FORGE_GENERATOR_SCHEMA_VERSION,build:String(build),product:plan.product,state:plan.packageState,publicationReady:plan.publicationReady,requirements:plan.requirements,guardrails:plan.guardrails})},
    {path:"native/ios-profile.json",data:json(iosProfile(plan,build))},
    {path:"content/README.md",data:`# Content input\n\n${plan.publicationReady?"This recipe uses the existing user-managed local vault. No user data is included in this generated package.":"Install a verified, versioned database and content-pack manifest before publication."}\n`},
    {path:"ASSET-REQUIREMENTS.md",data:plan.requirements.length?`# Requirements before publication\n\n${plan.requirements.map(item=>`- ${item}`).join("\n")}\n`:`# Requirements before publication\n\nNo recipe requirements are pending.\n`}
  );
  onProgress({phase:"zip",current:entries.length,total:entries.length,path:"package"});
  const archive=createDeterministicZip(entries);const fileName=`${plan.product.id}-appforge-pwa-build-${build}.zip`;
  return {blob:new Blob([archive],{type:"application/zip"}),bytes:archive,fileName,entries:entries.map(entry=>entry.path),plan};
}

