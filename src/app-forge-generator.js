function clone(value){return JSON.parse(JSON.stringify(value));}
function json(value){return `${JSON.stringify(value,null,2)}\n`;}
function bytes(value){return value instanceof Uint8Array?value:typeof value==="string"?encoder.encode(value):new Uint8Array(value);}
function extension(path){return String(path||"").split(".").pop().toLowerCase();}
function isText(path){return TEXT_EXTENSIONS.has(extension(path));}
function productNamespace(id="app"){return String(id||"app").toLowerCase().replace(/[^a-z0-9]+/g,"_").replace(/^_+|_+$/g,"")||"app";}
function html(value=""){return String(value).replace(/[&<>"']/g,char=>({"&":"&amp;","<":"&lt;",">":"&gt;",'"':"&quot;","'":"&#39;"}[char]));}
function wordmarkMarkup(profile){return (profile?.branding?.wordmark||[{text:profile?.name||"App",tone:"primary"}]).map((part,index)=>index===0?`<span>${html(part.text)}</span>`:`<b>${html(part.text)}</b>`).join("");}

